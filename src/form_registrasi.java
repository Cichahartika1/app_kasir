    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // koding tombol edit
        FRegistrasi s = new FRegistrasi ();
        try {
            stat = k.getCon().prepareStatement("update login set username=?, "+"password=?, privilege=? where Id_Login=?");
            stat.setString(1,s.username);
            stat.setString(2, s.password);
            stat.setInt(3, s.privilege);
            stat.setInt(4, Integer.parseInt(jTextField1.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di EDIT");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

