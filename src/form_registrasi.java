    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // koding tabel di klik
        int r = jTable1.getSelectedRow();
        String ID_USER = jTable1.getValueAt(r,0).toString();
        String USERNAME = jTable1.getValueAt(r,1).toString();
        String PASSWORD = jTable1.getValueAt(r,2).toString();
        String PRIVILEGE = jTable1.getValueAt(r,3).toString();
        
        jTextField1.setText(ID_USER);
        jTextField2.setText(USERNAME);
        jTextField3.setText(PASSWORD);
        jComboBox1.setName(PRIVILEGE);
    }//GEN-LAST:event_jTable1MouseClicked

